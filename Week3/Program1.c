//WAP to check whether a Number is Positive, Negative or Zero

#include <stdio.h>
int main(){
    int a  ;
    printf("Enter a Number: ");
    scanf("%d",&a);
    if (a > 0)
    {
        printf("The Number is Positive");
    }
    if (a < 0)
    {
        printf("The Number is Negative");
    }
    if (a == 0)
    {
        printf("The Number is Zero");
    }
return 0;

    
}
